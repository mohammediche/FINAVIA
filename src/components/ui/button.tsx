import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const buttonVariants = cva(
    "inline-flex items-center justify-center whitespace-nowrap rounded-full text-sm font-semibold ring-offset-background transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 active:scale-95",
    {
        variants: {
            variant: {
                default: "bg-[#e51990] text-white hover:bg-[#c01478] shadow-md hover:shadow-lg hover:shadow-[#e51990]/20",
                secondary: "bg-[#07036e] text-white hover:bg-[#0a058a]",
                outline: "border-2 border-[#07036e] bg-transparent text-[#07036e] hover:bg-[#07036e] hover:text-white",
                brandOutline: "border-2 border-[#e51990] bg-transparent text-[#e51990] hover:bg-[#e51990] hover:text-white",
                destructive: "bg-red-600 text-white hover:bg-red-700",
                ghost: "hover:bg-gray-100 text-[#07036e]",
                link: "text-[#e51990] underline-offset-4 hover:underline",
            },
            size: {
                default: "h-12 px-6 py-2",
                sm: "h-9 px-3",
                lg: "h-14 rounded-full px-10 text-base",
                icon: "h-10 w-10",
            },
        },
        defaultVariants: {
            variant: "default",
            size: "default",
        },
    } as const
)

export interface ButtonProps
    extends React.ButtonHTMLAttributes<HTMLButtonElement>,
        VariantProps<typeof buttonVariants> {
    asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
    ({ className, variant, size, asChild = false, ...props }, ref) => {
        const Comp = asChild ? Slot : "button"
        return (
            <Comp
                className={cn(buttonVariants({ variant, size, className }))}
                ref={ref}
                {...props}
            />
        )
    }
)
Button.displayName = "Button"

export { Button, buttonVariants }